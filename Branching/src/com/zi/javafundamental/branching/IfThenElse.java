package com.zi.javafundamental.branching;

public class IfThenElse {
    public static void main(String[] args) {
        boolean isOn = false;

        if (isOn) {
            System.out.println("Light is turn on");
        } else {
            System.out.println("Not happen");
        }
    }
}

// else bukan untuk secondary option, tapi last option
