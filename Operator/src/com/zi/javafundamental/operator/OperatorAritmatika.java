package com.zi.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args){
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 10 + 5;
        System.out.println("Hasil 10 + 5 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 10 - 5;
        System.out.println("Hasil 10 - 5 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Perkalian");
        int hasilPerkalian = 10 * 5;
        System.out.println("Hasil 10 * 5 = " + hasilPerkalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 10 / 5;
        System.out.println("Hasil 10 / 5 = " + hasilPembagian);
        System.out.println();

        // menggunakan sistem modulus (mod)
        System.out.println("Operasi Habis Bagi");
        int hasilSisa = 10 % 5;
        System.out.println("Hasil 10 % 5 = " + hasilSisa);
        int hasilSisaLain = 12 % 5;
        System.out.println("Hasil 12 % 5 = " + hasilSisaLain);
        System.out.println();
    }
}
